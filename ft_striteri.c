#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	s1;

	s1 = 0;
	if (s != NULL && f != NULL)
	{
		while (s[s1] != '\0')
		{
			f(s1, &s[s1]);
			s1++;
		}
	}
}
